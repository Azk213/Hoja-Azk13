# Subtraction
def sub(a, b):
    return a - b

# Addition
def add(a, b):
    return a + b

# Multiplication
def multiply(a, b):
    return a * b

# Division
def div(a, b):
    return a / b

# Exponential
def exp(a, b):
    return a ** b

# Square
def sqr(a):
    return a * a

# Area of circle with radius
def areaofcircle_r(a):
    return 3.14 * (a ** 2)

# Area of circle with diameter
def areaofcircle_d(a):
    return 3.14 * ((a / 2) ** 2)

# Even or Odd
def odd_or_even(a):
    if a % 2 == 0:
        print(a, "is Even")
    else:
        print(a, "is Odd")
    
    