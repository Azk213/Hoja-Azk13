#Substraction
def sub(a,b):
    return a-b

#Addition
def add(a,b):
    return a+b

#Multiplication
def multiply(a,b):
    return a*b

#Division
def div(a,b):
    return a*b

#Exponential
def exp(a,b):
    return a**b

#Square
def sqr(a):
    return a*a

#Area of circle with radius
def areaofcircle_r(a):
    return (a*3.14)**2

#Area of circle with diameter
def areaofcircle_d(a):
    return ((a/2)*3.14)**2

#Even or Odd
def odd_or_even(num):
    if num % 2 == 0:
        print(num,"is Even")
    else :
        print(num,"is Odd")

    
    